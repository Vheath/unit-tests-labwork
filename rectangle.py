def area(a, b): 
    # Принимает a, b - длины сторон прямоугольника
    # Возвращает площадь прямоугольника со сторонами a,b
    # Пример: print(area(2.5, 3.5))
    # Вывод: 8.75 
    return a * b 

def perimeter(a, b): 
    # Принимает a, b - длины сторон прямоугольника
    # Возвращает периметр прямоугольника со сторонами a,b 
    # Пример: print(perimeter(2.5, 3.5))
    # Вывод: 12.0    
    return 2*(a + b)
