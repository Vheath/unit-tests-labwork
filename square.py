
def area(a):
    # Принимает a - длина стороны квадрата
    # Возвращает площадь квадрата со стороной a
    # Пример: print(area(2.5))
    # Вывод: 6.25    
    return a * a


def perimeter(a):
    # Принимает a - длина стороны квадрата
    # Возвращает периметр квадрата со стороной a
    # Пример: print(perimeter(2.5))
    # Вывод: 10.0    
    return 4 * a
